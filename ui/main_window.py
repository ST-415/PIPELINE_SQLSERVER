                    self.after(0, lambda p=progress, f=os.path.basename(file_path), current=processed_files, total=total_files: 
                        self.progress_bar.update(p, f"‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•: {f}", f"‡πÑ‡∏ü‡∏•‡πå‡∏ó‡∏µ‡πà {current} ‡∏à‡∏≤‡∏Å {total}"))
                    
